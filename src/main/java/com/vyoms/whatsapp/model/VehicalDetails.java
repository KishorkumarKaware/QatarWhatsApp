package com.vyoms.whatsapp.model;

public class VehicalDetails {
	
	private String chassis_no = null;
	
	private String model = null;
	
	private String make = null;
	
	private String engine_no  = null;
	
	private String year = null; 
	
	private String cylenders = null;
	
	private String vehicalValue = null;

	private String name = null;
	
	private String email = null;
	
	private String contactNo = null;
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getContactNo() {
		return contactNo;
	}

	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}

	public String getChassis_no() {
		return chassis_no;
	}

	public void setChassis_no(String chassis_no) {
		this.chassis_no = chassis_no;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getEngine_no() {
		return engine_no;
	}

	public void setEngine_no(String engine_no) {
		this.engine_no = engine_no;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getCylenders() {
		return cylenders;
	}

	public void setCylenders(String cylenders) {
		this.cylenders = cylenders;
	}

	public String getVehicalValue() {
		return vehicalValue;
	}

	public void setVehicalValue(String vehicalValue) {
		this.vehicalValue = vehicalValue;
	}
	
	

}
